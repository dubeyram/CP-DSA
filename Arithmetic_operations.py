# Addition Operator
def add (a,b): 
    return a+b

# Subtract Operator
def sub(a,b):
    return a-b

# Multiply Operator
def mul (a,b):
    return a*b

# Modules Operator
def mod(a,b):
    return a%b

#Divide Operator
def div (a,b):
    return a/b

# Exponentiation Operator
def exp(a,b):
    return a**b

# Floor division Operator
def flrdiv(a,b):
    return a//b


